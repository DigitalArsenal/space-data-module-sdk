import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { transform } from "esbuild";
import { FlatcRunner } from "flatc-wasm";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageRoot = path.resolve(__dirname, "..");
const schemaRoot = path.join(packageRoot, "schemas", "orbpro");
const outputRoot = path.join(packageRoot, "src", "generated");

const entryFiles = [
  "BaseTypes.fbs",
  "CatalogQueryBase.fbs",
  "Plugin.fbs",
  "Propagator.fbs",
  "EntityMetadata.fbs",
  "StandardsRecordIndex.fbs",
  "EntityStandardsLink.fbs",
  "CatalogQueryRequest.fbs",
  "CatalogQueryResult.fbs",
];

const skippedBarrels = new Set(["orbpro/entity.ts", "orbpro/query.ts"]);

function asVirtualSchemaPath(fileName) {
  return `/schemas/orbpro/${fileName}`;
}

function addJsImportExtensions(code) {
  return code.replace(
    /((?:import|export)\s+[^'"]*?\sfrom\s+)(['"])(\.[^'"]*?)(\2)/g,
    (match, prefix, quote, specifier, suffix) => {
      if (/\.[cm]?js$/.test(specifier) || /\.json$/.test(specifier)) {
        return match;
      }
      return `${prefix}${quote}${specifier}.js${suffix}`;
    },
  );
}

async function loadSchemaTree() {
  const files = {};
  const fileNames = await fs.readdir(schemaRoot);
  for (const fileName of fileNames) {
    if (!fileName.endsWith(".fbs")) {
      continue;
    }
    files[asVirtualSchemaPath(fileName)] = await fs.readFile(
      path.join(schemaRoot, fileName),
      "utf8",
    );
  }
  return files;
}

function collectGeneratedTs(generatedFiles) {
  const collected = [];
  for (const [relativePath, content] of Object.entries(generatedFiles)) {
    if (!relativePath.startsWith("orbpro/") || !relativePath.endsWith(".ts")) {
      continue;
    }
    if (skippedBarrels.has(relativePath)) {
      continue;
    }
    collected.push([relativePath, content]);
  }
  return collected;
}

function setGeneratedFile(generatedFiles, relativePath, content) {
  const existing = generatedFiles.get(relativePath);
  if (existing !== undefined && existing !== content) {
    throw new Error(`Conflicting generated output for ${relativePath}`);
  }
  generatedFiles.set(relativePath, content);
}

function getManualBarrels() {
  return new Map([
    [
      "orbpro/entity.ts",
      `// generated by scripts/generate-orbpro-bindings.mjs\nexport { EntityKind } from "./entity/entity-kind.js";\nexport { EntityMetadata } from "./entity/entity-metadata.js";\nexport { StandardsRecordIndex } from "./entity/standards-record-index.js";\nexport { EntityStandardsLink } from "./entity/entity-standards-link.js";\n`,
    ],
    [
      "orbpro/query.ts",
      `// generated by scripts/generate-orbpro-bindings.mjs\nexport { CatalogQueryKind } from "./query/catalog-query-kind.js";\nexport { CatalogQueryRequest } from "./query/catalog-query-request.js";\nexport { CatalogQueryResult } from "./query/catalog-query-result.js";\n`,
    ],
  ]);
}

async function writeGeneratedFiles(generatedFiles) {
  for (const [relativePath, tsSource] of generatedFiles.entries()) {
    const tsPath = path.join(outputRoot, relativePath);
    const jsPath = tsPath.replace(/\.ts$/, ".js");
    await fs.mkdir(path.dirname(tsPath), { recursive: true });
    await fs.writeFile(tsPath, tsSource, "utf8");

    const transformed = await transform(tsSource, {
      loader: "ts",
      format: "esm",
      target: "es2020",
    });
    await fs.writeFile(
      jsPath,
      addJsImportExtensions(transformed.code),
      "utf8",
    );
  }
}

async function main() {
  const schemaTree = await loadSchemaTree();
  const flatc = await FlatcRunner.init();
  const generatedFiles = new Map();

  for (const entryFile of entryFiles) {
    const generated = flatc.generateCode(
      {
        entry: asVirtualSchemaPath(entryFile),
        files: schemaTree,
      },
      "ts",
    );
    for (const [relativePath, content] of collectGeneratedTs(generated)) {
      setGeneratedFile(generatedFiles, relativePath, content);
    }
  }

  for (const [relativePath, content] of getManualBarrels().entries()) {
    generatedFiles.set(relativePath, content);
  }

  await writeGeneratedFiles(generatedFiles);
  console.log(
    `Generated ${generatedFiles.size} OrbPro FlatBuffer TS+JS files into ${path.relative(packageRoot, outputRoot)}`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
